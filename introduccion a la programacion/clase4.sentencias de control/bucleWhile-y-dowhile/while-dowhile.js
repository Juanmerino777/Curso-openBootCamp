//bucle while:------------------------------------------

var numeroWhile = 0;
while (numeroWhile < 3) {
  numeroWhile++;
  console.log(numeroWhile);
}
/*BUCLES WHILE:
se crea una varible con valor.
declaramos la palabra clave WHILE y entre parenrecis colocamos la condicion.
despues entre corchetes colocamos la orden a seguir e imprimimos en consola  */

